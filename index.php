  <p>デプロイ方法</p>
  <ol>
    <li>GitHubにリポジトリを作成する</li>
    <li>リポジトリにコードをプッシュする</li>
    <li>Azureのポータルにログインする</li>
    <li>「新規リソースの作成」をクリックする</li>
    <li>「Web App」を選択する</li>
    <li>「リソースの作成」をクリックする</li>
    <li>「アプリ名」を入力する</li>
    <li>「リソースグループ」を選択する</li>
    <li>「OS」を選択する</li>
    <li>「App Service プラン」を選択する</li>
    <li>「App Service プランの作成」をクリックする</li>
    <li>「作成」をクリックする</li>
    <li>「デプロイ オプション」をクリックする</li>
    <li>「ソース」を選択する</li>
    <li>「GitHub」を選択する</li>
    <li>「GitHubの認証」をクリックする</li>
    <li>「リポジトリ」を選択する</li>
    <li>「ブランチ」を選択する</li>
    <li>「OK」をクリックする</li>
    <li>「デプロイ」をクリックする</li>
    <li>デプロイが完了するまで待つ</li>
  </ol>
